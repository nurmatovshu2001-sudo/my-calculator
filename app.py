df_phi.index = [int(str(idx).replace('S', '8')) for idx in df_phi.index]
df_phi.columns = [int(col) for col in df_phi.columns]

# --- БЛОК 3: ЛОГИКА ПОИСКА И РАСЧЕТА ---
ry_mpa = int(R * 10)

try:
    closest_col = min(df_phi.columns, key=lambda x: abs(x - ry_mpa))
    closest_row = min(df_phi.index, key=lambda x: abs(x - lambda_start))
    phi_raw_string = df_phi.loc[closest_row, closest_col]
    phi_integer = int(phi_raw_string.replace('S', '8'))
    phi = phi_integer / 1000
except Exception as e:
    phi = 1.0
    phi_integer = 1000
    phi_raw_string = "1000"

A_tr = (N * Yx) / (phi * R * Yy)
A_one = A_tr / 2

